// == (Comparação Implicita)
// === (Comparação Explicita)
const numero = 5;
const texto = "5";

console.log(`5 == "5": ${numero == texto}`); // true
console.log(`5 === "5": ${numero === texto}`); // false

// Typeof: Retorna o tipo de dado
console.log(`typeof numero: ${typeof numero}`);
console.log(`typeof texto: ${typeof texto}`);

// == compara somente o valor
// === compara o valor e o tipo de dado
